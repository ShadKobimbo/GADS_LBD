package com.example.gads_lbd.models;

public class Learner {

    public String learner_name;
    public String learner_hours;
    public String learner_country;
    public String learner_badge;

    public Learner(String learner_name, String learner_hours, String learner_country, String learner_badge) {
        this.learner_name = learner_name;
        this.learner_hours = learner_hours;
        this.learner_country = learner_country;
        this.learner_badge = learner_badge;

    }
}
